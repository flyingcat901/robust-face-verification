def apply_random_eraser_and_mask(eraser, input_img):
    # Batch size is always first dimension
    batch_size = input_img.shape[0]
    # Instantiate output arrays
    output_img  = np.zeros(input_img.shape)
    output_mask = np.zeros(input_img.shape)
    
    # Call eraser for each image
    for img_idx in range(batch_size):
        output_img[img_idx], output_mask[img_idx] = eraser(input_img[img_idx])

    return output_img, output_mask


